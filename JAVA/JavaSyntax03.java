public class JavaSyntax03 {
  // 1. 자바 구문 JavaSyntax

  // hello world 출력하기
  public static void main(String[] args) {
    System.out.println("Hello World!!!!!");
  }
  /*
    ** 자바 특징 **
      - java에서 실행된느 코드느 모두 <class> tag 내에 있어야 함
      - java는 대소문자를 구분함
      - java 파일명 == 클래스명 (일치해야 한다.)
      - public static void main(String[] args) {} => 단축코드 main 이다.
      - System.out.println() => 출력을 위한 메서드로 단축코드 sout 이다.
  */
}
